"""Verifiable-behavior (VB) coverage audit.

Test documents generated by ``codd generate`` declare a design-to-test
traceability table whose first column is a stable ``VB-<id>`` identifier, one
row per verifiable behavior. This module reconciles that declared universe
against ``codd: covers vb=<id>`` markers in test code, mirroring the
operational E2E audit semantics (explicit covers markers, explicit blockers,
orphan-marker reverse reconciliation).

The parser is header-text independent: any Markdown table whose first cell is
a ``VB-`` style id is a traceability row, so Japanese/English/any-language
headers all work. The marker grammar is shared with
``codd.operational_e2e_audit`` (subject alternation ``operation=``/``vb=``).
"""

from __future__ import annotations

import json
import re
from dataclasses import asdict, dataclass, field
from pathlib import Path, PurePosixPath
from typing import Any, Callable, Iterable, Sequence

from codd.frontmatter import codd_block, read_frontmatter
