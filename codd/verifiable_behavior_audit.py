# First-cell id pattern: `VB-` followed by an alphanumeric id body. Header
# rows ("#", "ID", "検証可能ビヘイビア", ...) and separator rows never match.
_VB_ID_RE = re.compile(r"^VB-[A-Za-z0-9][A-Za-z0-9_.-]*$", re.IGNORECASE)
_TABLE_ROW_RE = re.compile(r"^\s*\|(?P<cells>.+)\|\s*$")
_FENCE_RE = re.compile(r"^\s*(```|~~~)")
_DEFAULT_VB_DOC_DIR = "docs/test"
# A single ``vb=<id>`` token. A ``covers`` line may carry several of them
# (``# codd: covers vb=22 vb=23 vb=24``); the shared cover regex captures only
# the first and lets its greedy trailing-details group swallow the rest, so the
# VB audit re-scans the matched span with this token regex to recover every id.
_VB_TOKEN_RE = re.compile(r"vb\s*=\s*(?P<vb>[A-Za-z0-9_.:-]+)", re.IGNORECASE)

# A ``VB-*`` token appearing anywhere in free text / a non-first table cell — a
# REFERENCE to a canonical id, not a declaration. Anchored by id-boundary
# look-arounds so it matches a standalone token (``VB-CLI-007``) but not a
# substring inside a longer identifier. Mirrors the body shape of ``_VB_ID_RE``
# (which is whole-cell anchored) so a first-cell declaration and a later-column
# reference recover the SAME id text. Used only by :func:`parse_vb_references`.
_VB_REF_RE = re.compile(
    r"(?<![A-Za-z0-9_.-])VB-[A-Za-z0-9][A-Za-z0-9_.-]*(?![A-Za-z0-9_.-])",
    re.IGNORECASE,
)

# Canonical verifiable-behavior declaration doc. Exactly ONE generated test
# document owns the VB id namespace (declares every VB once in a first-column
# ``VB-*`` table); every other test doc references those canonical ids in a
# later column instead of minting a parallel ``VB-*`` table. This prevents two
# docs from declaring the same numeric id with different semantics — the
# structural defect that makes 100% coverage impossible (see
# parse_vb_table / load_verifiable_behaviors "first wins" dedup).
_CANONICAL_VB_NODE_ID = "test:test-strategy"
_CANONICAL_VB_OUTPUT_PATH = "docs/test/test_strategy.md"


def _first_cell_id_candidate(cell: str) -> str:
    """Normalize a table's first cell to a candidate VB id.

    A Markdown code span (`` `...` ``) escapes its interior from Markdown
    syntax entirely, so a trailing ``*`` inside one (e.g. a category-taxonomy
    summary row like `` `VB-TOK-*` ``, counting how many ``VB-TOK-NN`` ids
    exist) is literal text, not emphasis markup. Stripping backticks and
    ``*``/``_`` in one blind pass conflates the two and turns that wildcard
    marker into the atomic-looking ``VB-TOK-``, which then falsely matches
    ``_VB_ID_RE``. Backticks are therefore peeled first; only when the cell
    was NOT a code span are surrounding ``*``/``_`` (real emphasis markers)
    also stripped. A wildcard's trailing ``*`` then survives and correctly
    fails the atomic-id regex, so the row is silently ignored — the same
    outcome as any other non-VB table row.
    """

    value = cell.strip()
    in_code_span = len(value) >= 2 and value.startswith("`") and value.endswith("`")
    while len(value) >= 2 and value.startswith("`") and value.endswith("`"):
        value = value[1:-1].strip()
    if not in_code_span:
        value = value.strip("*_ ").strip()
    return value


def is_canonical_vb_doc(node_id: str | None = None, output_path: str | None = None) -> bool:
    """Whether a test doc is the canonical VB declaration document.

    The canonical doc (``test:test-strategy`` / ``docs/test/test_strategy.md``)
    is the sole owner of the VB id namespace. Matching is generic: any project
    whose test-strategy node/path uses the standard CoDD naming is canonical.
    Detection accepts either the node id or the output path so it works both at
    generation time (node id known) and audit time (only paths on disk).
    """

    if node_id is not None:
        normalized_node = node_id.strip().replace("\\", "/").casefold()
        if normalized_node == _CANONICAL_VB_NODE_ID:
            return True
    if output_path is not None:
        normalized_path = output_path.strip().replace("\\", "/").casefold()
        if normalized_path == _CANONICAL_VB_OUTPUT_PATH:
            return True
        # Tolerate an absolute or nested path whose tail is the canonical file.
        if normalized_path.endswith("/" + _CANONICAL_VB_OUTPUT_PATH):
            return True
        if PurePosixPath(normalized_path).name == "test_strategy.md":
            return True
    return False


def wave_config_plans_canonical_vb_doc(config: dict[str, Any] | None) -> bool:
    """Whether the project's ``wave_config`` plans the canonical VB registry doc.

    Structural signal that this project INTENDS to declare verifiable behaviors:
    one of its planned generation artifacts is the canonical VB document
    (``test:test-strategy`` / ``docs/test/test_strategy.md``). Mirrors the
    detection in ``codd.planner._set_canonical_vb_doc_config`` (which pins the
    canonical doc into ``test_coverage.docs`` at plan time). Generic — keys off
    the standard CoDD canonical naming, never any one project's specifics.
    """

    wave_config = (config or {}).get("wave_config")
    if not isinstance(wave_config, dict):
        return False
    for entries in wave_config.values():
        if not isinstance(entries, list):
            continue
        for entry in entries:
            if not isinstance(entry, dict):
                continue
            node_id = entry.get("node_id") or entry.get("id")
            output = entry.get("output")
            if is_canonical_vb_doc(
                node_id=node_id if isinstance(node_id, str) else None,
                output_path=output if isinstance(output, str) else None,
            ):
                return True
    return False


def project_expects_vb_registry(
    project_root: Path | str,
    config: dict[str, Any] | None = None,
) -> bool:
    """Whether this project is expected to own a canonical VB registry.

    The greenfield VB-registry gates (generate-time completeness gate and the
    implement-stage empty-registry hard-fail) apply ONLY to projects that have a
    verifiable-behavior SURFACE — never to a legitimate minimal project that
    genuinely declares no behaviors. A project "expects" a VB registry when ANY
    of these structural signals hold:

    1. its ``wave_config`` plans the canonical VB document
       (:func:`wave_config_plans_canonical_vb_doc`),
    2. ``test_coverage.docs`` is explicitly configured (the planner pins the
       canonical doc here, and an owner may set it directly), or
    3. a canonical VB document already exists among the discovered test docs.

    All three are generic/structural — no project proper-names. A project with
    none of these (e.g. an app whose plan declares only design docs and no test
    strategy) is NOT gated: it has no VB surface to certify, exactly as the
    brownfield "nothing to audit -> pass" rule already allows.
    """

    project_root = Path(project_root).resolve()
    if config is None:
        config = _load_optional_config(project_root)

    if wave_config_plans_canonical_vb_doc(config):
        return True

    section = (config or {}).get("test_coverage")
    if isinstance(section, dict) and section.get("docs"):
        return True

    for doc_path in discover_vb_documents(project_root, config=config):
        if is_canonical_vb_doc(output_path=_rel_path(doc_path, project_root)):
            return True
    return False


@dataclass(frozen=True)
class VBDeclarationIssue:
    """A VB-declaration coherence problem detected across test documents."""

    kind: str  # "collision" | "noncanonical_declaration" | "duplicate" | "range_shorthand"
    severity: str  # "error" | "warning"
    vb_id: str
    message: str
    docs: tuple[str, ...] = ()


@dataclass(frozen=True)
class VBReference:
    """A ``VB-*`` token REFERENCED (not declared) in a doc's later table cells.

    Distinct from :class:`VerifiableBehavior` (a first-column DECLARATION): a
    reference points AT a canonical id from somewhere else (an acceptance-
    criteria row, a traceability column). A reference is never a declaration —
    using one as a declaration source is the structural defect this module
    guards against (the dogfood failure where AC tables reference ``VB-*`` ids
    that the canonical registry never declares).
    """

    vb_id: str
    source_doc: str
    row_id: str = ""
    context: str = ""


@dataclass(frozen=True)
class VBRegistryIssue:
    """A canonical-VB-registry completeness problem (greenfield generate gate).

    Reported by :func:`validate_vb_registry_completeness`:

    * ``missing_canonical_doc`` — the canonical VB registry doc
      (``docs/test/test_strategy.md`` / ``test:test-strategy``), the VB
      namespace owner, is absent.
    * ``empty_canonical_registry`` — the canonical doc exists but declares zero
      first-column ``VB-*`` rows.
    * ``unresolved_reference`` — a ``VB-*`` id referenced in a non-canonical
      doc's later columns is not declared in the canonical doc.
    """

    kind: str  # "missing_canonical_doc" | "empty_canonical_registry" | "unresolved_reference"
    severity: str  # "error" | "warning"
    vb_id: str = ""
    source_doc: str = ""
    message: str = ""


# Range-shorthand atomicity: a VB id must name exactly ONE behavior. A
# declaration like ``VB-EVAL-02..06`` (an ellipsis range) or ``VB-02,03,04`` (a
# comma list) collapses several behaviors into one id, which destroys
# auditability — a single ``covers`` marker cannot prove five behaviors, and the
# range is never minted as atomic rows, so the "missing" members read as
# permanently uncovered. We forbid the SHORTHAND at declaration time and require
# the generator to emit one atomic row per behavior. (We do NOT eat the shorthand
# by expanding it on the marker side — that would re-introduce the false-coverage
# this gate exists to prevent.) The ``..`` ellipsis and an in-id comma are the
# unambiguous shorthand spellings; a plain hyphenated scheme (``VB-AUTH-1``) is
# left untouched.
_VB_RANGE_SHORTHAND_RE = re.compile(r"(\.\.|,|;|…|〜|～)")


def validate_vb_declarations(
    behaviors_by_doc: dict[str, list[VerifiableBehavior]],
    *,
    canonical_doc: str | None = None,
    strict: bool = False,
) -> list[VBDeclarationIssue]:
    """Validate that the declared VB namespace is coherent across documents.

    DECLARATION validation only — it never touches coverage semantics (no alias
    tables, no similarity matching). It reports:

    * **collision** (always ERROR): the same normalized VB id is declared in two
      or more docs with DIFFERENT descriptions — an incoherent namespace where
      one id means two things.
    * **duplicate** (always WARNING): the same id+description declared in more
      than one doc — redundant but coherent.
    * **noncanonical_declaration**: a non-canonical doc declares first-column
      ``VB-*`` rows. ERROR under ``strict`` (new/CI/greenfield), WARNING with
      migration guidance otherwise (existing brownfield docs).

    ``behaviors_by_doc`` maps each doc's display path to its parsed VB rows.
    ``canonical_doc`` is the display path of the canonical declarer when known;
    otherwise canonical-ness is inferred from the standard CoDD naming.
    """

    issues: list[VBDeclarationIssue] = []

    def _is_canonical(doc: str) -> bool:
        if canonical_doc is not None:
            return doc == canonical_doc
        return is_canonical_vb_doc(output_path=doc)

    # --- collisions / duplicates across docs (normalized id) ----------------
    grouped: dict[str, list[VerifiableBehavior]] = {}
    for doc, behaviors in behaviors_by_doc.items():
        for behavior in behaviors:
            grouped.setdefault(_normalize_vb_id(behavior.vb_id), []).append(behavior)

    for normalized_id, group in sorted(grouped.items()):
        docs = sorted({behavior.source_doc or "" for behavior in group})
        if len(docs) < 2:
            continue
        descriptions = {behavior.description.strip().casefold() for behavior in group}
        display_id = group[0].vb_id
        if len(descriptions) > 1:
            issues.append(
                VBDeclarationIssue(
                    kind="collision",
                    severity="error",
                    vb_id=display_id,
                    docs=tuple(docs),
                    message=(
                        f"VB id `{display_id}` is declared with different descriptions in "
                        f"multiple documents ({', '.join(docs)}); the VB namespace is "
                        "incoherent. Declare each verifiable behavior exactly once in the "
                        f"canonical doc (`{_CANONICAL_VB_OUTPUT_PATH}`) and reference it "
                        "elsewhere — never re-mint the same id with a different meaning."
                    ),
                )
            )
        else:
            issues.append(
                VBDeclarationIssue(
                    kind="duplicate",
                    severity="warning",
                    vb_id=display_id,
                    docs=tuple(docs),
                    message=(
                        f"VB id `{display_id}` is declared identically in multiple documents "
                        f"({', '.join(docs)}); keep a single canonical declaration and "
                        "reference it from the other doc(s)."
                    ),
                )
            )

    # --- range-shorthand atomicity (always ERROR) ---------------------------
    # A VB id that packs a range/list (``VB-EVAL-02..06``) is not atomic: it names
    # several behaviors under one id, so it can never be honestly covered by one
    # test marker. Flagged in EVERY doc (canonical or not) — atomicity is a
    # property of the id itself, independent of where it is declared.
    seen_range: set[tuple[str, str]] = set()
    for doc in sorted(behaviors_by_doc):
        for behavior in behaviors_by_doc[doc]:
            if not _VB_RANGE_SHORTHAND_RE.search(behavior.vb_id):
                continue
            key = (doc, behavior.vb_id)
            if key in seen_range:
                continue
            seen_range.add(key)
            issues.append(
                VBDeclarationIssue(
                    kind="range_shorthand",
                    severity="error",
                    vb_id=behavior.vb_id,
                    docs=(doc,),
                    message=(
                        f"VB id `{behavior.vb_id}` in `{doc}` uses range/list shorthand; a "
                        "verifiable-behavior id must be ATOMIC (name exactly one behavior). "
                        "Expand it into one row per behavior with a distinct id (e.g. "
                        "`VB-EVAL-02`, `VB-EVAL-03`, …). A range cannot be honestly covered by a "
                        "single `codd: covers vb=` marker, and its members would read as "
                        "permanently uncovered."
                    ),
                )
            )

    # --- non-canonical docs that declare first-column VB rows ---------------
    for doc in sorted(behaviors_by_doc):
        behaviors = behaviors_by_doc[doc]
        if not behaviors or _is_canonical(doc):
            continue
        sample = ", ".join(sorted({behavior.vb_id for behavior in behaviors})[:5])
        issues.append(
            VBDeclarationIssue(
                kind="noncanonical_declaration",
                severity="error" if strict else "warning",
                vb_id=sample,
                docs=(doc,),
                message=(
                    f"`{doc}` declares first-column VB id(s) ({sample}), but it is not the "
                    f"canonical VB document (`{_CANONICAL_VB_OUTPUT_PATH}`). Move every "
                    "auditable VB row into the canonical doc and convert this table to a "
                    "reference mapping (first column AC-*/requirement id; canonical VB ids "
                    "in a later column). Do not add duplicate markers to tests as the fix."
                ),
            )
        )

    return issues


def parse_vb_tables_by_doc(
    project_root: Path,
    *,
    config: dict[str, Any] | None = None,
    docs: Iterable[Path | str] | None = None,
) -> dict[str, list[VerifiableBehavior]]:
    """Parse each discovered test doc's VB table, keyed by display path.

    Unlike :func:`load_verifiable_behaviors` (which dedups across docs, "first
    wins"), this keeps per-doc declarations so a collision validator can see all
    sources of a given id.
    """

    project_root = Path(project_root).resolve()
    by_doc: dict[str, list[VerifiableBehavior]] = {}
    for doc_path in discover_vb_documents(project_root, config=config, docs=docs):
        try:
            text = doc_path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        display = _rel_path(doc_path, project_root)
        by_doc[display] = parse_vb_table(text, source_doc=display)
    return by_doc


