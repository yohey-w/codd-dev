def _scan_vb_markers(
    project_root: Path,
    *,
    test_dirs: Iterable[Path | str] | None,
) -> tuple[list[VBCoverEvidence], list[VBBlockerEvidence], list[tuple[str, str]]]:
    covers: list[VBCoverEvidence] = []
    blockers: list[VBBlockerEvidence] = []
    dod_markers: list[tuple[str, str]] = []  # (path, vb_id)
    for path in _iter_test_files(project_root, test_dirs=test_dirs):
        try:
            text = path.read_text(encoding="utf-8")
        except (OSError, UnicodeDecodeError):
            continue
        rel_path = _rel_path(path, project_root)
        for match in _COVER_MARKER_RE.finditer(text):
            first_vb = match.group("vb")
            if first_vb is None:
                continue  # operation-subject marker; owned by the operational audit
            # A single covers marker may list several `vb=` ids on one line
            # (`# codd: covers vb=22 vb=23 vb=24`): the shared regex captures
            # only the first and its greedy details group swallows the rest.
            # Recover every id, treating the non-`vb=` remainder as shared
            # details so all of them are reconciled, not just the first.
            raw_details = match.group("details") or ""
            extra_ids = [token.group("vb").strip() for token in _VB_TOKEN_RE.finditer(raw_details)]
            details = _VB_TOKEN_RE.sub("", raw_details).strip()
            for vb_id in [first_vb.strip(), *extra_ids]:
                covers.append(
                    VBCoverEvidence(
                        path=rel_path,
                        vb_id=vb_id,
                        details=details,
                    )
                )
        for match in _BLOCKER_MARKER_RE.finditer(text):
            vb_id = match.group("vb")
            if vb_id is None:
                continue
            blockers.append(
                VBBlockerEvidence(
                    path=rel_path,
                    vb_id=vb_id.strip(),
                    reason=match.group("reason").strip(),
                    details=(match.group("details") or "").strip(),
                )
            )
        for match in _DOD_MARKER_RE.finditer(text):
            vb_id = match.group("vb")
            if vb_id is None:
                continue
            dod_markers.append((rel_path, vb_id.strip()))
    return covers, blockers, dod_markers


def _audit_behavior(
    behavior: VerifiableBehavior,
    covers: list[VBCoverEvidence],
    blockers: list[VBBlockerEvidence],
) -> VBAuditRow:
    normalized = _normalize_vb_id(behavior.vb_id)
    matched = sorted({item.path for item in covers if _normalize_vb_id(item.vb_id) == normalized})
    matching_blockers = [item for item in blockers if _normalize_vb_id(item.vb_id) == normalized]
    primary_blocker = matching_blockers[0] if matching_blockers else None

    if primary_blocker is not None:
        status = "blocked"
        next_action = (
            "Resolve the explicit blocker, then replace the blocker marker with a covers marker."
        )
    elif matched:
        status = "covered"
        next_action = "Run the covering test(s) and keep the marker in sync on refactor."
    else:
        status = "uncovered"
        next_action = "Create or extend a test with a `codd: covers vb=` marker."

    return VBAuditRow(
        vb_id=behavior.vb_id,
        description=behavior.description,
        source_doc=behavior.source_doc,
        coverage_status=status,
        matched_tests=matched,
        blocker_reason=primary_blocker.reason if primary_blocker else "",
        blocker_details=primary_blocker.details if primary_blocker else "",
        blocker_evidence=sorted({item.path for item in matching_blockers}),
        suggested_next_action=next_action,
        declared_scenarios=behavior.declared_scenarios,
    )


def _detect_orphan_vb_markers(
    declared_ids: set[str],
    covers: list[VBCoverEvidence],
    blockers: list[VBBlockerEvidence],
    dod_markers: list[tuple[str, str]],
) -> list[OrphanVBMarker]:
    orphans: list[OrphanVBMarker] = []
    seen: set[tuple[str, str, str]] = set()

    def _check(path: str, vb_id: str, marker_kind: str) -> None:
        if _normalize_vb_id(vb_id) in declared_ids:
            return
        key = (path, _normalize_vb_id(vb_id), marker_kind)
        if key in seen:
            return
        seen.add(key)
        orphans.append(OrphanVBMarker(path=path, vb_id=vb_id, marker_kind=marker_kind))

    for item in covers:
        _check(item.path, item.vb_id, "covers")
    for blocker in blockers:
        _check(blocker.path, blocker.vb_id, "blocked")
    for path, vb_id in dod_markers:
        _check(path, vb_id, "dod")
    return orphans


# A leading ``vb`` is only stripped when it is a standalone token — i.e. the
# very next character is an id separator (``-``/``_``/``=``/space) or the value
# ends there. This makes the rule symmetric: ``VB-08`` and a bare marker value
# ``08`` converge, while a body that genuinely begins with ``vb`` (e.g.
# ``VB-vbx`` vs marker ``vbx``) is preserved on both sides instead of being
# eaten asymmetrically.
_LEADING_VB_TOKEN_RE = re.compile(r"^vb(?=[-_= \t]|$)")
_LEADING_SEPARATORS = "-_= \t"


def _normalize_vb_id(value: str) -> str:
    """Canonical matching key for a verifiable-behavior id.

    Declared ids (``VB-08``) and marker values (``vb=08`` → captured ``08``,
    or ``vb=VB-08`` → captured ``VB-08``) must reconcile to the same key. The
    canonical rule:

    1. casefold and strip surrounding whitespace,
    2. drop a single leading ``vb`` token (only when it stands alone, followed
       by a separator or end) plus any leading separators (``-``/``_``/``=``/
       space), so ``vb-08`` and bare ``08`` converge,
    3. for a purely-numeric remainder, strip leading zeros (``08`` → ``8``)
       so zero-padding never changes identity — while keeping distinct numbers
       distinct (``1`` stays ``1``, ``11`` stays ``11``: no collision),
    4. keep non-numeric remainders as-is (so schemes like ``VB-AUTH-1`` →
       ``auth-1`` still work).

    A value that reduces to nothing (e.g. a bare ``VB``) falls back to its
    casefolded form so it never collides with a genuinely empty key.
    """

    base = value.strip().casefold()
    remainder = _LEADING_VB_TOKEN_RE.sub("", base, count=1).lstrip(_LEADING_SEPARATORS)
    if not remainder:
        return base
    if remainder.isdigit():
        return remainder.lstrip("0") or "0"
    return remainder
