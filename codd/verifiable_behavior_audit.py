def coverage_gate_enabled(config: dict[str, Any] | None) -> bool:
    """Whether the implement-completion coverage gate is enabled (default ON)."""

    section = (config or {}).get("test_coverage")
    if isinstance(section, dict) and "gate" in section:
        return bool(section["gate"])
    return True


def coverage_gate_max_retries(config: dict[str, Any] | None) -> int:
    """Bounded re-implementation attempts before the gate fails (default 2)."""

    section = (config or {}).get("test_coverage")
    if isinstance(section, dict):
        value = section.get("max_retries")
        if isinstance(value, int) and value >= 0:
            return value
    return 2


def is_test_related_implement(
    config: dict[str, Any] | None,
    *,
    design_node: str | None,
    output_paths: Iterable[str],
) -> bool:
    """Whether an implement run targets test artifacts.

    True when any output path lives under a configured ``scan.test_dirs``
    entry (or carries a ``.spec.``/``.test.`` style filename), or the design
    node is a test-type document (``test:`` node id or a path under the
    ``docs/test`` doc-type directory).
    """

    if design_node:
        normalized = design_node.strip().replace("\\", "/")
        if normalized.startswith("test:"):
            return True
        parts = PurePosixPath(normalized).parts
        if len(parts) >= 3 and parts[0] == "docs" and parts[1] == "test":
            return True

    raw_dirs = ((config or {}).get("scan") or {}).get("test_dirs")
    test_dirs = [str(item) for item in raw_dirs] if isinstance(raw_dirs, list) and raw_dirs else ["tests/"]
    prefixes = [item.replace("\\", "/").strip("/") for item in test_dirs if str(item).strip("/")]
    for raw_path in output_paths:
        rel = str(raw_path).replace("\\", "/").strip("/")
        if not rel:
            continue
        for prefix in prefixes:
            if rel == prefix or rel.startswith(prefix + "/"):
                return True
        name = PurePosixPath(rel).name
        if re.search(r"\.(spec|test)\.[A-Za-z0-9]+$", name):
            return True
    return False


def format_gap_feedback(report: VBAuditReport) -> str:
    """Render uncovered VBs as review feedback for a bounded re-implementation."""

    lines = [
        "The generated tests do not cover every declared verifiable behavior.",
        "For EACH behavior listed below, add (or extend) a test that proves it and "
        "annotate that test with a comment marker of the exact form "
        "`codd: covers vb=<ID>` (one marker per behavior, placed inside the test file).",
        "If a behavior genuinely cannot be tested yet, add an explicit "
        "`codd: blocked vb=<ID> reason=<short_reason>` marker instead — never leave it silently uncovered.",
        "",
        "Uncovered verifiable behaviors:",
    ]
    for row in report.uncovered_rows:
        description = f" — {row.description}" if row.description else ""
        lines.append(f"- {row.vb_id}{description} (declared in {row.source_doc})")
    return "\n".join(lines)


def run_implement_coverage_gate(
    project_root: Path | str,
    *,
    config: dict[str, Any] | None = None,
    design_node: str | None,
    output_paths: Iterable[str],
    opt_out: bool = False,
    rerun: Callable[[str], None] | None = None,
    echo: Callable[[str], None] = print,
    echo_error: Callable[[str], None] | None = None,
) -> bool:
    """Audit VB coverage after an implement run; retry with feedback, then gate.

    Returns ``True`` when the gate passes (or does not apply) and ``False``
    when uncovered VBs remain after the bounded retries. Projects without any
    VB table are unaffected: the gate announces that and passes.
    """

    project_root = Path(project_root).resolve()
    if config is None:
        config = _load_optional_config(project_root)
    if opt_out or not coverage_gate_enabled(config):
        return True
    if not is_test_related_implement(config, design_node=design_node, output_paths=output_paths):
        return True

    emit_error = echo_error or echo
    report = build_vb_coverage_audit(project_root, config=config)
    if not report.rows:
        echo("Test coverage gate: no VB table found in test documents — nothing to audit.")
        return True

    max_retries = coverage_gate_max_retries(config)
    attempt = 0
    while report.uncovered_rows and rerun is not None and attempt < max_retries:
        attempt += 1
        echo(
            f"Test coverage gate: {len(report.uncovered_rows)} uncovered verifiable behavior(s); "
            f"re-running implementation with gap feedback (attempt {attempt}/{max_retries})"
        )
        rerun(format_gap_feedback(report))
        report = build_vb_coverage_audit(project_root, config=config)

    for orphan in report.orphan_vb_markers:
        emit_error(orphan.message)
    if report.uncovered_rows:
        emit_error(
            f"Test coverage gate FAILED: {len(report.uncovered_rows)} of {report.summary['vb_count']} "
            "verifiable behavior(s) have no `codd: covers vb=` marker:"
        )
        for row in report.uncovered_rows:
            description = f" — {row.description}" if row.description else ""
            emit_error(f"  ✗ {row.vb_id}{description} ({row.source_doc})")
        emit_error(
            "Add a covering test with a `codd: covers vb=<ID>` marker, declare an explicit "
            "`codd: blocked vb=<ID> reason=...` marker, or opt out with --no-coverage-gate."
        )
        return False

    echo(
        f"Test coverage gate: {report.summary['covered']} covered / "
        f"{report.summary['blocked']} blocked / {report.summary['vb_count']} verifiable behavior(s) — OK"
    )
    return True


